public class Loops{
    public static void main(String args[]){

        // for loop
        for(int i = 1; i<= 10; i++){
           System.out.println(i);
        }


         // while loop
         int i = 1;
        while(i<=10){
            System.out.println(i);
            i++;
       }

       // do while loop
       i = 1;
       do{
        System.out.println(i);
        i = i+1;
       }while(i<11);
    }
}